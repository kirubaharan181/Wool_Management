    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id1 = jTextField5.getText();
    String det = jTextField6.getText();
    String rate = jTextField7.getText();
    String quan1 = jTextField8.getText();
    int quantity = Integer.parseInt(quan1);
    int qu = Integer.parseInt(jTextField13.getText());
    int price = Integer.parseInt(rate);
    int total = price * quantity;

    double a = 0.01;
    finalTotal = finalTotal + total;
    credit = a * finalTotal;
    String finalTotal1 = String.valueOf(finalTotal);
    String creditamount = String.valueOf(credit);

    Connection con = null;
    try {
        con = ConnectionProvider.getCon();
        con.setAutoCommit(false); // Start transaction

        Statement st = con.createStatement();

        // Fetch current quantity from the producer table
        ResultSet rs = st.executeQuery("SELECT quan FROM producer WHERE id = '" + id1 + "'");
        if (rs.next()) {
            int currentQuantity = rs.getInt("quan");
            int newQuantity = currentQuantity - quantity;

            if (newQuantity < 0) {
                JOptionPane.showMessageDialog(null, "Error: Not enough stock available.");
                return;
            }

            // Insert the transaction details into the trend table
            if (quantity > 0 && quantity <= qu) {
                st.executeUpdate("INSERT INTO trend (Id, Name, Rate, Quantitiy) VALUES ('" + id1 + "','" + det + "','" + rate + "','" + quan1 + "')");

                // Update the quantity in the producer table
                st.executeUpdate("UPDATE producer SET quan = " + newQuantity + " WHERE id = '" + id1 + "'");

                con.commit(); // Commit the transaction

                // Update UI components
                jTextField10.setText(finalTotal1);
                jTextField9.setText(creditamount);
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.addRow(new Object[]{det, price, quantity, total});
                DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
                model2.addRow(new Object[]{id1, det, rate, newQuantity});
                
                JOptionPane.showMessageDialog(null, "Product successfully added and stock updated.");
            } else {
                JOptionPane.showMessageDialog(null, "Not sufficient product.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Error: Product ID not found.");
        }
    } catch (Exception e) {
        try {
            if (con != null) {
                con.rollback(); // Rollback transaction on error
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        JOptionPane.showMessageDialog(null, e);
    } finally {
        try {
            if (con != null) {
                con.setAutoCommit(true); // Restore default auto-commit mode
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

