private void addPaidAmountListener() {
    jTextField11.getDocument().addDocumentListener(new DocumentListener() {
        public void changedUpdate(DocumentEvent e) {
            updateReturnAmount();
        }
        public void removeUpdate(DocumentEvent e) {
            updateReturnAmount();
        }
        public void insertUpdate(DocumentEvent e) {
            updateReturnAmount();
        }

        public void updateReturnAmount() {
            try {
                int paidAmount = Integer.parseInt(jTextField11.getText());
                int totalAmount = Integer.parseInt(jTextField10.getText());
                int returnAmount = paidAmount - totalAmount;
                jTextField12.setText(String.valueOf(returnAmount));
            } catch (NumberFormatException ex) {
                // Handle the case where the input is not a valid integer
                jTextField12.setText("");
            }
        }
    });
}

