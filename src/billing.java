    private void printDetails() {
        PrinterJob job = PrinterJob.getPrinterJob();
        job.setPrintable(this);
        boolean doPrint = job.printDialog();
        if (doPrint) {
            try {
                job.print();
            } catch (PrinterException ex) {
                JOptionPane.showMessageDialog(null, "Failed to print: " + ex.getMessage());
            }
        }
    }
    
        public int print(Graphics g, PageFormat pf, int page) throws PrinterException {
    if (page > 0) {
        return NO_SUCH_PAGE;
    }

    Graphics2D g2d = (Graphics2D) g;
    g2d.translate(pf.getImageableX(), pf.getImageableY());

    int x = 100; // Starting x-coordinate for drawing
    int y = 100; // Starting y-coordinate for drawing
    int width = 400; // Width of the bill
    int height = 400; // Height of the bill
    int padding = 20; // Padding for the text inside the border

    // Draw outline for the bill
    g2d.drawRect(x, y, width, height);

    // Define dimensions and position for the company info box
    int companyBoxHeight = 60; // Height of the company info box
    int companyBoxPadding = 10; // Padding inside the company info box

    // Draw the company info box
    g2d.drawRect(x, y, width, companyBoxHeight);

    // Draw Company Name and Address inside the company info box
    g.drawString("Company Name: XYZ Pvt Ltd", x + companyBoxPadding, y + companyBoxPadding + 10);
    g.drawString("Address: 123 Main Street, City, Country", x + companyBoxPadding, y + companyBoxPadding + 30);

    // Adjust y-coordinate for the main content to start below the company info box
    y += companyBoxHeight + 10;

    // Draw Date & Time section
    g.drawString("Date & Time: " + jLabel1.getText() + " & " + jLabel21.getText(), x + padding, y + padding);
    y += 20;

    // Draw Buyer Details section
    g.drawString("Buyer Details:", x + padding, y + padding);
    y += 20;
    g.drawString("Name: " + jTextField1.getText(), x + padding, y + padding);
    y += 20;
    g.drawString("Id: " + jTextField2.getText(), x + padding, y + padding);
    y += 20;
    g.drawString("Address: " + jTextField4.getText(), x + padding, y + padding);
    y += 20;

    // Draw Table header
    g.drawString(String.format("%-10s %-10s %-10s %-10s", "Name", "Rate", "Quantity", "Total"), x + padding, y + padding);
    y += 20;

    // Draw table rows
    for (int i = 0; i < jTable1.getRowCount(); i++) {
        String n = jTable1.getValueAt(i, 0).toString();
        String r = jTable1.getValueAt(i, 1).toString();
        String q = jTable1.getValueAt(i, 2).toString();
        String s = jTable1.getValueAt(i, 3).toString();
        g.drawString(String.format("%-10s %-10s %-10s %-10s", n, r, q, s), x + padding, y + padding);
        y += 20;
    }

    // Draw Total, Paid Amount, Return Amount, and Credit Amount sections
    g.drawString("Total: " + jTextField10.getText(), x + padding, y + padding);
    y += 20;
    g.drawString("Paid Amount: " + jTextField11.getText(), x + padding, y + padding);
    y += 20;
    g.drawString("Return Amount: " + jTextField12.getText(), x + padding, y + padding);
    y += 20;
    g.drawString("Credit Amount: " + jTextField9.getText(), x + padding, y + padding);
    y += 20;

    // Draw Thank you message
    g.drawString("Thank you for visiting.", x + padding, y + padding);

    return PAGE_EXISTS;
}


